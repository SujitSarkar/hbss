import 'package:maori_health/domain/client/entities/address.dart';

class AddressModel extends Address {
  const AddressModel({
    required super.id,
    required super.userId,
    required super.operatorId,
    required super.homeStreetAddress,
    required super.homeSuburb,
    required super.homeCity,
    required super.homePostCode,
    required super.homeSa3Region,
    required super.homeLatitude,
    required super.homeLongitude,
    required super.postalStreetAddress,
    required super.postalSuburb,
    required super.postalCity,
    required super.postalPostCode,
    required super.postalSa3Region,
    required super.postalLatitude,
    required super.postalLongitude,
    required super.createdAt,
    required super.updatedAt,
    required super.deletedAt,
    required super.fullAddress,
  });

  factory AddressModel.fromJson(Map<String, dynamic> json) => AddressModel(
    id: json["id"],
    userId: json["user_id"],
    operatorId: json["operator_id"],
    homeStreetAddress: json["home_street_address"],
    homeSuburb: json["home_suburb"],
    homeCity: json["home_city"],
    homePostCode: json["home_post_code"],
    homeSa3Region: json["home_SA3_region"],
    homeLatitude: json["home_latitude"],
    homeLongitude: json["home_longitude"],
    postalStreetAddress: json["postal_street_address"],
    postalSuburb: json["postal_suburb"],
    postalCity: json["postal_city"],
    postalPostCode: json["postal_post_code"],
    postalSa3Region: json["postal_SA3_region"],
    postalLatitude: json["postal_latitude"],
    postalLongitude: json["postal_longitude"],
    createdAt: json["created_at"] == null ? null : DateTime.parse(json["created_at"]),
    updatedAt: json["updated_at"] == null ? null : DateTime.parse(json["updated_at"]),
    deletedAt: json["deleted_at"],
    fullAddress: json["full_address"],
  );

  Map<String, dynamic> toJson() => {
    "id": id,
    "user_id": userId,
    "operator_id": operatorId,
    "home_street_address": homeStreetAddress,
    "home_suburb": homeSuburb,
    "home_city": homeCity,
    "home_post_code": homePostCode,
    "home_SA3_region": homeSa3Region,
    "home_latitude": homeLatitude,
    "home_longitude": homeLongitude,
    "postal_street_address": postalStreetAddress,
    "postal_suburb": postalSuburb,
    "postal_city": postalCity,
    "postal_post_code": postalPostCode,
    "postal_SA3_region": postalSa3Region,
    "postal_latitude": postalLatitude,
    "postal_longitude": postalLongitude,
    "created_at": createdAt?.toIso8601String(),
    "updated_at": updatedAt?.toIso8601String(),
    "deleted_at": deletedAt,
    "full_address": fullAddress,
  };
}
